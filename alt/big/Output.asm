(copy_start)
// DST = start of third row of screen buffer
@SCREEN
D=A
@240
D=D+A
@DST
M=D
// SRC = @table_start (start of the table in ROM)
@table_start
D=A
@SRC
M=D

(copy_loop)
// D = MEM[SRC++]
@SRC
M=M+1
A=M-1
D=M
// MEM[DST++] = D
@DST
M=M+1
A=M-1
M=D

// Check for end of table:
@SRC
D=M
@table_end
D=D-A
@copy_loop
D;JLE


(halt)
@halt
0;JMP

